const nonEnglishLocales = ["es", "fr", "de", "ar", "it", "ru", "ja", "zh", "hi"];
let foundAny = false;

for (const loc of nonEnglishLocales) {
  const fp = path.join(__dirname, "..", "messages", loc + ".json");
  const raw = fs.readFileSync(fp, "utf8");
  for (const pattern of englishPatterns) {
    if (raw.includes(pattern)) {
      const idx = raw.indexOf(pattern);
      const start = Math.max(0, idx - 40);
      const end = Math.min(raw.length, idx + pattern.length + 40);
      const ctx = raw
        .substring(start, end)
        .replace(/\n/g, " ")
        .trim();
      // Try to find the key path
      const beforeKey = raw.substring(Math.max(0, idx - 200), idx);
      const keyMatch = beforeKey.match(/"([^"]+)"\s*:\s*$/m);
      const keyInfo = keyMatch ? ` (near key: ...${keyMatch[1]})` : "";
      console.log(
        `${loc}: English pattern "${pattern}"${keyInfo}\n  context: ...${ctx}...\n`
      );
      foundAny = true;
    }
  }
}
if (!foundAny) {
  console.log("No remaining English text patterns found in non-English locales!");
}
